/**
  * @brief  The application entry point.
  * @retval int
  */
int main(void)
{
  /* USER CODE BEGIN 1 */

  /* USER CODE END 1 */

  /* Enable I-Cache---------------------------------------------------------*/
  SCB_EnableICache();

  /* Enable D-Cache---------------------------------------------------------*/
  SCB_EnableDCache();

  /* MCU Configuration--------------------------------------------------------*/

  /* Reset of all peripherals, Initializes the Flash interface and the Systick. */
  HAL_Init();

  /* USER CODE BEGIN Init */

  /* USER CODE END Init */

  /* Configure the system clock */
  SystemClock_Config();

  /* USER CODE BEGIN SysInit */

  /* USER CODE END SysInit */

  /* Initialize all configured peripherals */
  MX_GPIO_Init();
  MX_DMA_Init();
  MX_I2C2_Init();
  MX_DCMI_Init();
  MX_LPTIM1_Init();
  /* USER CODE BEGIN 2 */

  OV7670_Power(DISABLE);
  Init_LCD_GPIO();
  SSD1963_Init();
  SSD1963_ClearScreen(0);
  //I2C2 is used for SCCB and LPTIM1 for XLCK generation
  OV7670_Init(&hdcmi, &hdma_dcmi, &hi2c2, &hlptim1);

  OV7670_PowerUp();
  OV7670_UpdateSettings(OV7670_settings);
  OV7670_SetFrameRate(XCLK_DIV(1), PLL_x4);
  HAL_Delay(10);

  OV7670_Start(CONTINUOUS, image_data);
  OV7670_getImageInfo(&width,&height,&format);
  HAL_Delay(100);



  /* USER CODE END 2 */

  /* Infinite loop */
  /* USER CODE BEGIN WHILE */


  while (1)
  {

    /* USER CODE END WHILE */

    /* USER CODE BEGIN 3 */

	for(k=0;k<2;k++) //interleved drawing - image looks more fluent
	{
		for(i=k;i<height;i++)
		{
			//Write directly to display (SSD1963), don't use GUI - FASTEST!!!
			SSD1963_SetArea(X_START, X_START+(width*PX_SIZE)-1, Y_START+i*PX_SIZE, Y_START+(i+1)*PX_SIZE);
			SSD1963_WriteCommand(SSD1963_WRITE_MEMORY_START);

			for(ph=0;ph<PX_SIZE;ph++)
			{
				for(j = 0; j < (((width)/2)); j++)
				{
					temp=image_data[j+i*(width/2)];  // temp contains 2 pixel values

					if(format==YUV422)	//YUV422
					{
						//Extract YUV
						  Y2=(temp>>24)&0x00FF;
						  U=((temp>>16)&0x00FF)-128;
						  Y1=(temp>>8)&0x00FF;
						  V=(temp&0x00FF)-128;
						  for(pw=0;pw<PX_SIZE;pw++)SSD1963_WriteData(YUVtoRGB565(Y1,U,V));
						  for(pw=0;pw<PX_SIZE;pw++)SSD1963_WriteData(YUVtoRGB565(Y2,U,V));
					}
					else				//RGB565
					{
						for(pw=0;pw<PX_SIZE;pw++)SSD1963_WriteData(temp&0xFFFF);
						for(pw=0;pw<PX_SIZE;pw++)SSD1963_WriteData((temp>>16)&0xFFFF);
					}
				}
			}
			i++; //skip 1 line - interleved drawing
		}
	}
}
  /* USER CODE END 3 */
}

